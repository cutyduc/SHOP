export const getProductsByCategoryAndGender = (products, category, gender) => {
  return products.filter((product) => {
    const matchCategory = category ? product.category.includes(category) : true;
    const matchGender = gender ? product.gender === gender : true;
    return matchCategory && matchGender;
  });
};
